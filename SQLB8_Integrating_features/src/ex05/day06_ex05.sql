COMMENT ON TABLE person_discounts IS 
'Таблица хранит информацию о персональных скидках клиентов в пиццериях.
Каждая запись представляет собой уникальную скидку для конкретного клиента в конкретной пиццерии.
Используется для программы лояльности и персонализации цен.';

COMMENT ON COLUMN person_discounts.id IS 
'Уникальный идентификатор записи. 
Первичный ключ таблицы.';


COMMENT ON COLUMN person_discounts.person_id IS 
'Идентификатор клиента. 
Внешний ключ, ссылается на таблицу person. 
Не может быть NULL.';

COMMENT ON COLUMN person_discounts.pizzeria_id IS 
'Идентификатор пиццерии. 
Внешний ключ, ссылается на таблицу pizzeria. 
Не может быть NULL.';

COMMENT ON COLUMN person_discounts.discount IS 
'Размер персональной скидки в процентах. 
Значение от 0 до 100 включительно. 
По умолчанию: 0.0 (0% скидки). 
Не может быть NULL.';